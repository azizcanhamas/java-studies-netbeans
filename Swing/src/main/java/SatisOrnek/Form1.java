    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        String urunAdi=jComboBox1.getSelectedItem().toString();
        String fiyatStr=jTextField1.getText();
        String pesinatStr=jTextField2.getText();
        String taksitStr=String.valueOf(jSpinner1.getValue());
       
        if(fiyatStr==""){
            jLabel5.setText("Fiyat bilgisi boş geçilemez!");
            return;
        }
        else if(pesinatStr==""){
            jLabel5.setText("Peşinat bilgisi boş geçilemez!");
            return;
        }
        
        int fiyat=Integer.parseInt(fiyatStr);
        int pesinat=Integer.parseInt(pesinatStr);
        int taksit=Integer.parseInt(taksitStr);
        
        int aylikOdeme=(fiyat-pesinat)/taksit;
        
        String lastStr="Ürün adı: "+urunAdi+"\n"+
                       "Fiyat: "+fiyat+"\n"+
                       "Peşinat: "+pesinat+"\n"+
                       "Taksit: "+taksit+"\n"+
                       "Aylık ödeme: "+aylikOdeme;
        
        Form2 frm2=new Form2(lastStr);
        frm2.setVisible(true);
    }//GEN-LAST:event_jToggleButton1ActionPerformed

