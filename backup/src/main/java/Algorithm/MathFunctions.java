package Algorithm;

public class MathFunctions {
    public static void main(String[] args) {
        
        //MATH FONKSIYONLARI
        
//====================================================

        //PI sayisi tanimlidir.
        System.out.println(Math.PI);      

        //Soldaki sayinin sagdaki kuvvetini doner.
        System.out.println(Math.pow(2, 3));

        //Sayinin kokunu alir.
        System.out.println(Math.sqrt(25));

        //Sayinin mutlagini alir.
        System.out.println(Math.abs(-1));    

        //Iki sayidan kucuk olani dondurur.
        System.out.println(Math.min(100, -1));

        //Iki sayidan buyuk olani dondurur.
        System.out.println(Math.max(100, -1));

        //Sayiyi en yakin tam sayiya yuvarlar.
        System.out.println(Math.round(3.7));

        //Sayiyi asagiya yuvarlar.
        System.out.println(Math.floor(3.4));

        //Sayiyi yukariya yuvarlar.
        System.out.println(Math.ceil(3.4));

        //0...1 araligindan random bir sayi uretir.
        System.out.println(Math.random());

        //Sayinin sinusunu alir.
        System.out.println(Math.sin(90));

        //Sayinin cosinusunu alir.
        System.out.println(Math.cos(90));

        //Sayinin tanjantini alir.
        System.out.println(Math.tan(90));

        //Sayinin arcsinusunu alir.
        System.out.println(Math.asin(0.25));

        //Sayinin arccosinusunu alir.
        System.out.println(Math.acos(0.25));

        //Sayinin arctanini alir.
        System.out.println(Math.atan(90));

        //E sayisi tanimlidir.
        System.out.println(Math.E);

        //e tabaninda bir sayinin logaritmasini alir.
        System.out.println(Math.log(10));

        //e sayisinin kuvvetini alir.
        System.out.println(Math.exp(10));

//====================================================
    }
    
}
